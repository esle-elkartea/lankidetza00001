    private void establecerRestricciones() {
        jTextFieldReferencia.setDocument(new LimitadorCaracteres(jTextFieldReferencia, 25, false, false));
        jTextFieldDescripcion1.setDocument(new LimitadorCaracteres(jTextFieldDescripcion1, 25, false, false));
        jTextFieldDescripcion2.setDocument(new LimitadorCaracteres(jTextFieldDescripcion2, 25, false, false));
        jTextFieldDescripcion3.setDocument(new LimitadorCaracteres(jTextFieldDescripcion3, 25, false, false));
    }
        
    private boolean valida(){
        boolean resultado = true;
        this.mensaje=null;
        resultado = resultado && validaReferencia();
        resultado = resultado && validaDescripcion1();
        resultado = resultado && validaDescripcion2();
        resultado = resultado && validaDescripcion3();
        if(!resultado){
            // Mostrar mensaje
            Mensaje.error(bundle.getString("ERRORES_EN_DATOS_INTRODUCIDOS")+this.mensaje+"\n");
        }
        return resultado;
    }
       
    private boolean validaReferencia(){
        boolean resultado = true;
        String valor = jTextFieldReferencia.getText();
        String label = jLabelReferencia.getText();
        if(valor==null || "".equals(valor)){
            // Mensaje campo obligatorio
            if(mensaje==null)mensaje="";
            mensaje+= bundle.getString("EL_CAMPO")+" "+label+" "+bundle.getString("ES_OBLIGATORIO")+"\n";
            resultado = false;
        }
        return resultado;
    }
    
