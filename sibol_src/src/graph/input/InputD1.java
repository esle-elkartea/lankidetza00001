    public void installValue(D1Bean valor) {
        datosD1 = valor;
        
        if(datosD1 != null) {
            jTextFieldReferencia.setText(String.valueOf(datosD1.getReferencia()));
            jComboBoxNumeroPolos.setSelectedItem(DecimalFormat.getInstance().format(datosD1.getNumeroPolos()));
            jComboBoxCalibre.setSelectedItem(DecimalFormat.getInstance().format(datosD1.getCalibre()));
            recalcularOpcionesSensibilidad();
            jComboBoxSensibilidad.setSelectedItem(DecimalFormat.getInstance().format(datosD1.getSensibilidad()));
        } else {
            recalcularOpcionesSensibilidad();
        }
        
    }
    
