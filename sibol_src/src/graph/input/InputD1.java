    private void jBtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAceptarActionPerformed
        if(valida()){
            String referencia = (String)jTextFieldReferencia.getText();
            double numeroPolos = 0;
            double calibre = 0;
            double sensibilidad = 0;
            try {
                numeroPolos = Double.parseDouble(((String)jComboBoxNumeroPolos.getSelectedItem()).replace(",","."));
                calibre = Double.parseDouble(((String)jComboBoxCalibre.getSelectedItem()).replace(",","."));
                sensibilidad = Double.parseDouble(((String)jComboBoxSensibilidad.getSelectedItem()).replace(",","."));
            } catch(NumberFormatException e) {}
            datosD1.setReferencia(referencia);
            datosD1.setNumeroPolos(numeroPolos);
            datosD1.setCalibre(calibre);
            datosD1.setSensibilidad(sensibilidad);
            datosD1.setEditado(true);
            cellEditor.stopCellEditing();
        }
    }//GEN-LAST:event_jBtnAceptarActionPerformed
    
