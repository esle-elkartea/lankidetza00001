    private void recalcularOpcionesSensibilidad(){
        String numeroPolos = (String)jComboBoxNumeroPolos.getSelectedItem();
        String calibre = (String)jComboBoxCalibre.getSelectedItem();
        if("2".equals(numeroPolos) && "25".equals(calibre)){
            jComboBoxSensibilidad.removeAllItems();
            jComboBoxSensibilidad.addItem("10");
            jComboBoxSensibilidad.addItem("30");
            jComboBoxSensibilidad.addItem("300");
            jComboBoxSensibilidad.addItem("500");
            jComboBoxSensibilidad.setSelectedIndex(-1);
        } else if(("2".equals(numeroPolos) && "40".equals(calibre)) ||
                  ("2".equals(numeroPolos) && "63".equals(calibre)) ||
                  ("2".equals(numeroPolos) && "80".equals(calibre)) ||
                  ("4".equals(numeroPolos) && "25".equals(calibre)) ||
                  ("4".equals(numeroPolos) && "40".equals(calibre)) ||
                  ("4".equals(numeroPolos) && "63".equals(calibre))){
            jComboBoxSensibilidad.removeAllItems();
            jComboBoxSensibilidad.addItem("30");
            jComboBoxSensibilidad.addItem("300");
            jComboBoxSensibilidad.addItem("500");
            jComboBoxSensibilidad.setSelectedIndex(-1);
        } else if("4".equals(numeroPolos) && "80".equals(calibre)){
            jComboBoxSensibilidad.removeAllItems();
            jComboBoxSensibilidad.addItem("300");
            jComboBoxSensibilidad.addItem("500");
            jComboBoxSensibilidad.setSelectedIndex(-1);
        } else if(("2".equals(numeroPolos) && "100".equals(calibre)) ||
                  ("4".equals(numeroPolos) && "100".equals(calibre))){
            jComboBoxSensibilidad.removeAllItems();
            jComboBoxSensibilidad.addItem("300");
            jComboBoxSensibilidad.setSelectedIndex(-1);
        }
    }
    
