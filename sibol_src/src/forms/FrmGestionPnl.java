    private void cambiarMunicipio(JComboBox jCmbBxMunicipio, JComboBox jCmbBxProvincia){
        ResultSet rs = null;
        String consulta;
        jCmbBxMunicipio.removeAllItems();
        
        ParCombo combo = (ParCombo)jCmbBxProvincia.getSelectedItem();
        
        try{
            if(combo.getKeyInt() == 0)
                jCmbBxMunicipio.addItem(new ParCombo("0",""));
            else{
                consulta = "SELECT * FROM MUNICIPIOS WHERE MUPRID="+combo.getKeyInt()+" ORDER BY MUNOM";
                rs = bd.ejecSelect(consulta);
                while(rs.next())
                    jCmbBxMunicipio.addItem(new ParCombo(rs.getInt("MUID"),rs.getString("MUNOM")));
            }
        }
        catch(SQLException e){
            Mensaje.error("FrmGestionPnl.java: "+e.getMessage(),e);
        }      
    }
    

    private void cambiarLocalidad(JComboBox jCmbBxLocalidad, JComboBox jCmbBxMunicipio, JComboBox JCmbBxProvincia){
        ResultSet rs = null;
        String consulta;
        
        jCmbBxLocalidad.removeAllItems();
        
        if(jCmbBxMunicipio.getSelectedItem() != null){
            ParCombo parComboProvincia = (ParCombo)JCmbBxProvincia.getSelectedItem();
            ParCombo parComboMunicipio = (ParCombo)jCmbBxMunicipio.getSelectedItem();
            
            try{
                if(parComboProvincia.getKeyInt() == 0 || parComboProvincia.getKeyInt() == 0)
                    jCmbBxLocalidad.addItem(new ParCombo("0",""));
                else{
                    consulta = "SELECT * FROM LOCALIDADES WHERE LCPRID="+parComboProvincia.getKeyInt()+" AND LCMUID="+parComboMunicipio.getKeyInt()+" ORDER BY LCNOM";
                    rs = bd.ejecSelect(consulta);
                    while(rs.next())
                        jCmbBxLocalidad.addItem(new ParCombo(rs.getInt("LCID"),rs.getString("LCNOM")));
                }
                
            } catch(Exception e){
                Mensaje.error("FrmGestionPnl.java: "+e.getMessage(),e);
            }
        }
    }
    
    private void cambiarCP(JComboBox JCmbBxMunicipio, JComboBox JCmbBxProvincia, JTextField jTxtFldCp) {
        String consulta;
        
        if(JCmbBxMunicipio.getSelectedItem() != null){
            ParCombo parComboMunicipio = (ParCombo)JCmbBxMunicipio.getSelectedItem();
            ParCombo parComboProvincia = (ParCombo)JCmbBxProvincia.getSelectedItem();
            
            ResultSet rs = null;
            
            try{
                if(parComboProvincia.getKeyInt() == 0 || parComboMunicipio.getKeyInt() == 0)
                    jTxtFldCp.setText("");
                else{
                    consulta = "SELECT * FROM MUNICIPIOS WHERE MUPRID="+parComboProvincia.getKeyInt()+" AND MUID="+parComboMunicipio.getKeyInt();
                    rs = bd.ejecSelect(consulta);
                    rs.next();
                    jTxtFldCp.setText(rs.getString("MUCP"));
                }  
            }
            catch(Exception e){
                Mensaje.error("FrmGestionPnl.java: "+e.getMessage(),e);
            }
        }
    }
    
