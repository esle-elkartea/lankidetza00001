    //Agrega a los Jcombox del formulario los datos tomados de la base de datos.
    private void agregarDatosCombox() {
        ResultSet rs = null;
        try{
            rs = bd.ejecSelect("SELECT * FROM PROVINCIAS ORDER BY PRNOM");
            jCmbBxProvinciaTitular.addItem(new ParCombo(0, ""));
            jCmbBxProvinciaInst.addItem(new ParCombo(0, ""));
            jCmbBxEmpSuministradora.addItem(new ParCombo(0,0,""));
            while(rs.next()){
                nomProv = rs.getString("PRNOM");
                codProv = rs.getInt("PRID");
                jCmbBxProvinciaTitular.addItem(new ParCombo(codProv, nomProv));
                jCmbBxProvinciaInst.addItem(new ParCombo(codProv, nomProv));
            }
            
            //Hay que agregar antes las modalidades de instalacin antes que las categoras del instalador.
            rs = bd.ejecSelect("SELECT * FROM MODALIDAD_INST ORDER BY MIID");
            while(rs.next())
                jCmbBxModalidadEmp.addItem(new ParCombo(rs.getString(1), rs.getString(2)));
            rs = bd.ejecSelect("SELECT * FROM CAT_INSTALADOR ORDER BY CIDESC");
            while(rs.next())
                jCmbBxCatInst.addItem(new ParCombo(rs.getString(1), rs.getString(2)));
            
            //Carga de empresas suministradoras
            rs = bd.ejecSelect("SELECT * FROM EMP_SUMINISTRADORAS ORDER BY ESNOMBRE");
            while(rs.next())
                jCmbBxEmpSuministradora.addItem(new ParCombo(rs.getInt(1), rs.getInt(2),rs.getString(3)));
        } catch(SQLException e){
            Mensaje.error("FrmGestionPnl.java: "+e.getMessage(),e);
        }
        
        //Los comboboxes son autocompletables
        new AutoComplCmbBxRestrictivo(jCmbBxLocalidadTitular);
        new AutoComplCmbBxRestrictivo(jCmbBxLocalidadInst);
        new AutoComplCmbBxRestrictivo(jCmbBxMunicipioInst);
        new AutoComplCmbBxRestrictivo(jCmbBxMunicipioTitular);
        new AutoComplCmbBxRestrictivo(jCmbBxProvinciaTitular);
        new AutoComplCmbBxRestrictivo(jCmbBxProvinciaInst);
        new AutoComplCmbBxRestrictivo(jCmbBxCatInst);
        new AutoComplCmbBxRestrictivo(jCmbBxModalidadEmp);
        new AutoComplCmbBxRestrictivo(jCmbBxEmpSuministradora);
        
    }
