    private void jBttnAceptar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnAceptar1ActionPerformed
        if(esValido())
        {
            if(esActualizar)
                this.actualizarBD();
            else
                this.introducirBD();
            
            insertarValoresFrmGestion();
            frmGestion.setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
        else
            Mensaje.error(txtError);
    }//GEN-LAST:event_jBttnAceptar1ActionPerformed
    
    public boolean esValido()
    {
        txtError = "\n * Error en la introducción de los datos:\n";
        boolean esCorrecto=true;
        if(jTxtFldNombreTec.getText().equals("")){
            esCorrecto=false;
            txtError += "      - NOMBRE no debe de estar vacío.\n";
        }
        if(jTxtFldTituloTec.getText().equals("")){
            esCorrecto=false;
            txtError += "      - TÍTULO TÉCNICO no debe de estar vacío.\n";
        }
        if(jTxtFldNumColegiado.getText().equals("")){
            esCorrecto=false;
            txtError += "      - Nº COLEGIADO no debe de estar vacío.\n";
        }
        if(jTxtFldNIF.getText().equals("")){
            esCorrecto=false;
            txtError += "      - DNI no debe de estar vacío.\n";
        }
        if(!Utilidades.esDniNifValido(jTxtFldNIF.getText()))
        {
            esCorrecto=false;
            txtError += "      - DNI no contiene un valor válido.\n";
        }
        if(jTxtFldColegioOficial.getText().equals("")){
            esCorrecto=false;
            txtError += "      - COLEGIO OFICIAL no debe de estar vacío.\n";
        }
        
        return esCorrecto;
          
    }
    
    private void introducirBD() {
        ResultSet rs = null;
        nombre = jTxtFldNombreTec.getText();
        titulo = jTxtFldTituloTec.getText();
        numero = jTxtFldColegioOficial.getText();
        nif = jTxtFldNIF.getText();
        colegio = jTxtFldNumColegiado.getText();
        
        BaseDatos bd = Sesion.getInstance().getBaseDatos();
        String consulta ="INSERT INTO TECNICOS_CUALIFICADOS (TCNOMBRE, TCTITULO, TCNUMCOLEGIADO, TCNIF, TCCOLEGIO)" +
                " VALUES('"+nombre+"','"+titulo+"','"+numero+"','"+nif+"','"+colegio+"')";
        
        try {
            bd.ejecModificacion(consulta);
            consulta = "SELECT * FROM TECNICOS_CUALIFICADOS";
            rs = bd.getUltimoResgistro(consulta);
            
            //Guardar en la sesión la ID de SES_TECNICOS_CUALIFICADOS_ID
            Sesion.getInstance().setValorHt(Constantes.SES_TECNICOS_CUALIFICADOS_ID, rs.getString(Constantes.SES_TECNICOS_CUALIFICADOS_ID));   
        } 
        catch (SQLException e) {
            Mensaje.error("DlgNuevoTecnico.java. "+e.getMessage(),e);
        } 
    }

    private void insertarValoresFrmGestion() {
        frmGestion.jTxtFldNombreTec.setText(nombre);
        frmGestion.jTxtFldTituloTec.setText(titulo);
        frmGestion.jTxtFldNumColegiado.setText(numero);
        frmGestion.jTxtFldNIF.setText(nif);
        frmGestion.jTxtFldColegioOficial.setText(colegio);
    }

    public void establecerRestricciones()
    {
        jTxtFldNombreTec.setDocument(new LimitadorCaracteres(jTxtFldNombreTec, 40,false, false));
        jTxtFldTituloTec.setDocument(new LimitadorCaracteres(jTxtFldTituloTec, 50,false, false));
        jTxtFldNumColegiado.setDocument(new LimitadorCaracteres(jTxtFldNumColegiado, 10,false, false));
        jTxtFldNIF.setDocument(new LimitadorCaracteres(jTxtFldNIF, 9,false, false));
        jTxtFldColegioOficial.setDocument(new LimitadorCaracteres(jTxtFldColegioOficial, 50,false, false));
    }
    
