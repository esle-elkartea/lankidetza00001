    private void actualizarBD() {
        ResultSet rs = null;
        nombre  = jTxtFldNombreTec.getText();
        titulo  = jTxtFldTituloTec.getText(); 
        numero  = jTxtFldNumColegiado.getText();
        nif  = jTxtFldNIF.getText();
        colegio = jTxtFldColegioOficial.getText();
       
       
        BaseDatos bd = Sesion.getInstance().getBaseDatos();
        String consulta ="UPDATE TECNICOS_CUALIFICADOS SET TCNOMBRE='"+nombre+"', " +
                " TCTITULO='"+titulo+"', TCNUMCOLEGIADO='"+numero+"', TCNIF='"+nif+"', TCCOLEGIO='"+colegio+"' " +
                " WHERE TCID="+Sesion.getInstance().getValorHt(Constantes.SES_TECNICOS_CUALIFICADOS_ID).toString();
        
        try {
            bd.ejecModificacion(consulta);   
        } 
        catch (SQLException e) {
            Mensaje.error("DlgNuevoTecnico.java. "+e.getMessage(),e);
        }
    }
    
