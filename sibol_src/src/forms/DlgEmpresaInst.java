    private void jBttnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnBuscarActionPerformed
        ResultSet rs = null;
        String consulta = "";
        String nombreSocial = this.jTxtFldSocial.getText().toUpperCase();
        String instalador = this.jTxtFldInstalador.getText().toUpperCase();
        
        if(nombreSocial.equals("") && instalador.equals(""))
            consulta = "SELECT * FROM INSTALADORES";
        else if(nombreSocial.equals(""))
            consulta = consulta="SELECT * FROM INSTALADORES WHERE  UPPER(ITINSTALADOR) LIKE '"+instalador.toUpperCase()+"%'";
        else if(instalador.equals(""))
            consulta = consulta="SELECT * FROM INSTALADORES WHERE  UPPER(ITNOMBREEMP) LIKE '"+nombreSocial.toUpperCase()+"%'";
        else
            consulta = consulta="SELECT * FROM INSTALADORES WHERE  UPPER(ITINSTALADOR) LIKE '"+instalador.toUpperCase()+"%' AND ITNOMBREEMP LIKE '"+nombreSocial+"%'";

        try{
            ModeloTablaNoEditable modelo = null;
            rs = bd.ejecSelect(consulta);
            Object [] col = new Object[9];
            if(rs.next())
            {
                modelo = new ModeloTablaNoEditable();
                this.jTblInst.setModel(modelo);
                
                modelo.addColumn("Nombre / Razón social");
                modelo.addColumn("Nº Empresa / EIBT");
                modelo.addColumn("Nombre instalador");
                modelo.addColumn("Nº Carné / CCBT");
                modelo.addColumn("DNI");
                modelo.addColumn("Cat. Instalador");
                modelo.addColumn("Modalidad");
                modelo.addColumn("Tfno");
                modelo.addColumn("E-mail");
                
                rs = bd.ejecSelect(consulta);
                while (rs.next())
                {
                    col[0] = new ParCombo(rs.getString(Constantes.SES_INSTALADORES_ID),rs.getString("ITNOMBREEMP"));
                    col[1] = rs.getString("ITNUMEMP1") +" / "+ rs.getString("ITNUMEMP2");
                    col[2] = rs.getString("ITINSTALADOR");
                    col[3] = rs.getString("ITCARNET1") +" / "+ rs.getString("ITCARNET2");
                    col[4] = rs.getString("ITNIF");
                    col[5] = rs.getString("ITCATEGORIA");
                    col[6] = rs.getString("ITMODALIDAD");
                    col[7] = rs.getString("ITTELEFONO");
                    col[8] = rs.getString("ITEMAIL");
                    modelo.addRow(col);                    
                }
            }
            else
            {
                jTblInst.setModel(new ModeloTablaNoEditable(
                new Object [][] {},new String [] {"Nombre / Razón social", "Nº Empresa / EIBT", "Nombre instalador", "Nº Carné / CCBT", "DNI", "Cat. Instalador", "Modalidad", "Tfno", "E-mail"}
                ));
                Mensaje.aviso("No existe ningún registro con el criterio de búsqueda elegido");
            }
        }
        catch(Exception e){
            Mensaje.error("DlgEmpresaInst.java: "+e.getMessage(),e);
        }
    }//GEN-LAST:event_jBttnBuscarActionPerformed

    private void insertarValoresFrmGestion() {
        ResultSet rs = null;
        String consulta = "SELECT * FROM INSTALADORES WHERE ITID="+pcFilaSeleccionada.getKeyString();
        String idModalidad, idCategoria;
        ParCombo pcMod, pcCat, pcTemp;
        
        try{
            rs = bd.ejecSelect(consulta);
            rs.next();
            //Guardar en la sesión la ID de INSTALADORES
            Sesion.getInstance().setValorHt(Constantes.SES_INSTALADORES_ID, rs.getString(Constantes.SES_INSTALADORES_ID));
            frmGestion.jTxtFldNombreEmpresa.setText(rs.getString("ITNOMBREEMP"));
            frmGestion.jTxtFldNumEmpresa.setText(rs.getString("ITNUMEMP1"));
            frmGestion.jTxtFldEibt.setText(rs.getString("ITNUMEMP2"));
            frmGestion.jTxtFldNombreInst.setText(rs.getString("ITINSTALADOR"));
            frmGestion.jTxtFldCcbt.setText(rs.getString("ITCARNET2"));
            frmGestion.jTxtFldNumCarne.setText(rs.getString("ITCARNET1"));
            frmGestion.jTxtFldTelefonoEmp.setText(rs.getString("ITTELEFONO"));
            frmGestion.jTxtFldEmailEmp.setText(rs.getString("ITEMAIL"));
            frmGestion.jTxtFldNIFInstalador.setText(rs.getString("ITNIF"));
            idModalidad = rs.getString("ITMODALIDAD");
            idCategoria = rs.getString("ITCATEGORIA");
            //Procedo a rellenar los combos
            rs = bd.ejecSelect("SELECT * FROM MODALIDAD_INST WHERE MIID='"+idModalidad+"'");
            rs.next();
            pcMod = new ParCombo(rs.getString(1),rs.getString(2));
            rs = bd.ejecSelect("SELECT * FROM CAT_INSTALADOR WHERE CIID='"+idCategoria+"'");
            rs.next();
            pcCat = new ParCombo(rs.getString(1),rs.getString(2));
            
            for(int i=0; i<frmGestion.jCmbBxModalidadEmp.getItemCount(); i++)
            {
                pcTemp = (ParCombo)frmGestion.jCmbBxModalidadEmp.getItemAt(i);
                if(pcTemp.getKeyString().equals(pcMod.getKeyString()))
                    frmGestion.jCmbBxModalidadEmp.setSelectedItem(pcTemp);
            }
        
            for(int i=0; i<frmGestion.jCmbBxCatInst.getItemCount(); i++)
            {
                    pcTemp = (ParCombo)frmGestion.jCmbBxCatInst.getItemAt(i);
                    if(pcTemp.getKeyString().equals(pcCat.getKeyString()))
                        frmGestion.jCmbBxCatInst.setSelectedItem(pcTemp);
            }
        }
        catch(SQLException e){
            Mensaje.error("DlgEmpresaInst.java. "+e.getMessage(),e);
        }
    }

