    private void insertarValoresFrmGestion() {
        ResultSet rs = null;
        String consulta = "SELECT * FROM TECNICOS_CUALIFICADOS WHERE TCID="+pcFilaSeleccionada.getKeyString();
        
        try{
            rs = bd.ejecSelect(consulta);
            rs.next();
            //Guardar en la sesión la ID de TECNICOS_CUALIFICADOS
            Sesion.getInstance().setValorHt(Constantes.SES_TECNICOS_CUALIFICADOS_ID, rs.getString(Constantes.SES_TECNICOS_CUALIFICADOS_ID));
            frmGestion.jTxtFldNombreTec.setText(rs.getString("TCNOMBRE"));
            frmGestion.jTxtFldTituloTec.setText(rs.getString("TCTITULO"));
            frmGestion.jTxtFldNumColegiado.setText(rs.getString("TCNUMCOLEGIADO"));
            frmGestion.jTxtFldColegioOficial.setText(rs.getString("TCCOLEGIO"));
            frmGestion.jTxtFldNIF.setText(rs.getString("TCNIF"));
        }
        catch(SQLException e){
            Mensaje.error("DlgSeleccionarTecnico.java. "+e.getMessage(),e);
        }
    }

