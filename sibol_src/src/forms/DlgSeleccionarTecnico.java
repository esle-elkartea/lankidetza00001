    private void jBttnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnBuscarActionPerformed
        ResultSet rs = null;
        String consulta = "";
        String nombreTecnico = this.jTxtFldNombreTecnico.getText();
        
        if(nombreTecnico.equals(""))
            consulta = "SELECT * FROM TECNICOS_CUALIFICADOS";
        else
            consulta = consulta="SELECT * FROM TECNICOS_CUALIFICADOS WHERE UPPER(TCNOMBRE) LIKE '"+nombreTecnico.toUpperCase()+"%'";
        
        try{
            ModeloTablaNoEditable modelo = null;
            rs = bd.ejecSelect(consulta);
            Object [] col = new Object[8];
            if(rs.next()) {
                modelo = new ModeloTablaNoEditable();
                this.jTbTecnico.setModel(modelo);
                
                modelo.addColumn("Nombre técnico");
                modelo.addColumn("Título");
                modelo.addColumn("Nº Colegiado");
                modelo.addColumn("DNI");
                modelo.addColumn("Colegio oficial");
                
                rs = bd.ejecSelect(consulta);
                while (rs.next()) {
                    col[0] = new ParCombo(rs.getString(Constantes.SES_TECNICOS_CUALIFICADOS_ID),rs.getString("TCNOMBRE"));
                    col[1] = rs.getString("TCTITULO");
                    col[2] = rs.getString("TCNUMCOLEGIADO");
                    col[3] = rs.getString("TCNIF");
                    col[4] = rs.getString("TCCOLEGIO");
                    modelo.addRow(col);
                }
            } else {
                jTbTecnico.setModel(new ModeloTablaNoEditable(
                        new Object [][] {},new String [] {"Nombre técnico", "Título", "Nº Colegiado", "DNI", "Colegio oficial"}));
                Mensaje.aviso("No existe ningún registro con el criterio de búsqueda elegido");
            }
        } catch(Exception e){
            Mensaje.error("DlgSeleccionarTecnico.java: "+e.getMessage(),e);
        }
    }//GEN-LAST:event_jBttnBuscarActionPerformed

